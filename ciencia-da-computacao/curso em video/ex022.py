frase = 'Curso em video Python'

print(frase[15])  # pega o caractere na posição 15
print(frase[:15])  # pega tudo até o caractere 15
print(frase[15:])  # pega tudo DEPOIS do caractere 15
print(frase[9:14])  # pega tudo entre 9 e 14 -- excluindo o 14 -- o ultimo valor não entra na contagem
print(frase[9:14:2])  # pega do 9 ao 14 pulando de 2 em 2
print(frase[9::3])  # do 9 ate o final pulando de 3 em 3

len(frase)  # comprimento da frase

print(frase.count('o'))  # conta quantas vezes aparece o 'o' na frase





